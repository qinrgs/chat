export let keycode = (d, v) => {
    if (d.indexOf("-") > -1) {
        //多键 keyObj
        let ds = d.split("-")
        let df = []
        ds.map(es => {
            df.push(keyObj[es])
        })
        if (df.length == 2) v(df[0], df[1], "0")
        if (df.length == 3) v(df[0], df[1], df[2])
    } else {
        v(keyObj[d], "0", "0")
    }
}